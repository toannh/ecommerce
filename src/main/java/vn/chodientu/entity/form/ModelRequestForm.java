package vn.chodientu.entity.form;

/**
 *
 * @author thanhvv
 */
public class ModelRequestForm {

    private String id;
    private String asignedEmail;
    private String message;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAsignedEmail() {
        return asignedEmail;
    }

    public void setAsignedEmail(String asignedEmail) {
        this.asignedEmail = asignedEmail;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

}
